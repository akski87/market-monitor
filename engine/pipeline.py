def clean():
    """Sweep bad rows left by bad pulls: (1) purge rows stamped with an
    IMPOSSIBLE FUTURE date — residue of the pre-ET UTC clock bug, whose ghost
    snapshots shadow fresh data in every MAX(snapshot_date) lookup (observed:
    run #27's '2026-06-12' rows hid run #28's corrected 06-11 rows, so the
    dashboard showed pre-SF-store data); (2) delete listings with neither an
    asking nor a net rent (extractor junk); then sync snapshots.units_captured
    and recompute market_history for every affected date.
    Idempotent — safe to run every day; reports what it removed."""
    removed_future = 0
    with conn() as c:
        today = today_et()
        fut = [r[0] for r in c.execute(
            "SELECT DISTINCT snapshot_date FROM listings WHERE snapshot_date > ? "
            "UNION SELECT DISTINCT snapshot_date FROM snapshots WHERE snapshot_date > ?",
            (today, today))]
        for d in sorted(fut):
            n = c.execute("SELECT COUNT(*) FROM listings WHERE snapshot_date=?", (d,)).fetchone()[0]
            print(f"clean: PURGING future-dated snapshot {d} ({n} listings) — UTC clock-bug residue; today (ET) is {today}.")
            c.execute("DELETE FROM listings WHERE snapshot_date=?", (d,))
            c.execute("DELETE FROM snapshots WHERE snapshot_date=?", (d,))
            c.execute("DELETE FROM market_history WHERE snapshot_date=?", (d,))
            removed_future += n
    with conn() as c:
        bad = c.execute("""SELECT id, building_id, snapshot_date, unit FROM listings
                           WHERE asking_rent IS NULL AND net_effective_rent IS NULL""").fetchall()
        if not bad:
            print(f"clean: nothing else to remove." if removed_future else "clean: nothing to remove.")
            return removed_future
        dates = sorted({r["snapshot_date"] for r in bad})
        pairs = sorted({(r["building_id"], r["snapshot_date"]) for r in bad})
        for r in bad:
            print(f"clean: removing {r['building_id']} {r['snapshot_date']} unit {r['unit']} (no rents)")
        c.execute("""DELETE FROM listings
                     WHERE asking_rent IS NULL AND net_effective_rent IS NULL""")
        for bid, d in pairs:   # snapshot counts reflect reality after the sweep
            n = c.execute("SELECT COUNT(*) FROM listings WHERE building_id=? AND snapshot_date=?",
                          (bid, d)).fetchone()[0]
            c.execute("UPDATE snapshots SET units_captured=? WHERE building_id=? AND snapshot_date=?",
                      (n, bid, d))
        for d in dates:        # recompute the trend point for each touched date
            _recompute_market_point(c, d, "Recomputed by clean().")
    print(f"clean: removed {len(bad)} row(s) across {len(pairs)} building-date(s); "
          f"market_history recomputed for {dates}.")
    return len(bad) + removed_future

